# Câu 4: Khái niệm JSON
  # JSON (JavaScript Object Notation) là định dạng dữ liệu sử dụng cặp key-value để lưu trữ và truyền dữ liệu
  #JSON có nguồn gốc từ JavaScript nhưng là định dạng dữ liệu độc lập với ngôn ngữ 
  # Nhờ định dạng đơn giản, dễ sử dụng và truy vấn, JSON được ứng dụng phổ biến trong việc lưu trữ và trao đổi dữ liệu


# Câu 5: Hạn chế của việc triển khai JSON trong Python
  # Không hỗ trợ các kiểu dữ liệu phức tạp như tuple, set, datetime, và các đối tượng lớp tùy chỉnh
  # Độ chính xác của số liệu kém, không phân biệt số nguyên và số thực
  # Hiệu suất hạn chế, khi xử lý một khối lượng lớn dữ liệu, tốc độ xử lý JSON có thể chậm

