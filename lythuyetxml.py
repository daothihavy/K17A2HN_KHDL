# Câu 1: Khái niệm XML
  # XML (Extensible Markup Language) là ngôn ngữ đánh dấu mở rộng do W3C phát triển, dùng để lưu trữ và mô tả dữ liệu. 
  # XML cho phép người dùng tự định nghĩa các thẻ tag, giúp truyền và chia sẻ dữ liệu giữa các hệ thống khác nhau một cách linh hoạt.


# Câu 2: Khái niệm DOM
  # DOM (Document Object Model) là mô hình đối tượng tài liệu
  # Cho phép truy xuất và thao tác nội dung, cấu trúc của tài liệu HTML hoặc XML dưới dạng đối tượng
  # Nó hỗ trợ các ngôn ngữ lập trình để thao tác tài liệu một cách linh hoạt và có cấu trúc


